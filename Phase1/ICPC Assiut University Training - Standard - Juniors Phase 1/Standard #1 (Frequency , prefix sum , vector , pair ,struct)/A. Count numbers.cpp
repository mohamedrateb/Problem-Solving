class func
{
public:
    ull nCr(int n, int r)
    {
        if (r > n)
            return 0;
        r = max(r, n - r);
        unsigned long long ans = 1, div = 1, i = r + 1;
        while (i <= n)
        {
            ans *= i;
            i++;
            ans /= div;
            div++;
        }
        return ans;
    }

    ull nPr(int n, int r)
    {
        if (r > n)
            return 0;
        unsigned long long p = 1, i = n - r + 1;
        while (i <= n)
            p *= i++;
        return p;
    }
    ull fact(ull num)
    {
        if (num == 0 || num == 1)
            return 1;
        return num * fact(num - 1);
    }
    ull gcd(ull a, ull b)
    {
        if (b == 0)
        {
            return a;
        }
        return gcd(b, a % b);
    }
    ull lcm(ull a, ull b)
    {
        return a / gcd(a, b) * b;
    }
    bool isprime(ll num)
    {
        if (num == 1)
            return false;
        // if (num == 2 || num == 3) return true;
        // if (num % 2 == 0 || num % 3 == 0)return false;
        for (int i = 2; i * i <= num; i++)
        {
            if (num % i == 0)
                return false;
        }
        return true;
    }
};

